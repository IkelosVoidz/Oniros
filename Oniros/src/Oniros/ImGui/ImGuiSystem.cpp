	void ImGuiSystem::Init()
	{
		// Setup Dear ImGui context
		IMGUI_CHECKVERSION();
		ImGui::CreateContext();
		ImGuiIO& io = ImGui::GetIO(); (void)io;

		io.ConfigFlags |= ImGuiConfigFlags_NavEnableKeyboard;       // Enable Keyboard Controls
		io.ConfigFlags |= ImGuiConfigFlags_DockingEnable;           // Enable Docking
		io.ConfigFlags |= ImGuiConfigFlags_ViewportsEnable;         // Enable Multi-Viewport / Platform Windows
		
		//io.ConfigViewportsNoAutoMerge = true;
		//io.ConfigViewportsNoTaskBarIcon = true;


		//TODO : Revisit this when imports are working
		/*ImFont* pFont = io.Fonts->AddFontFromFileTTF("C:\\Windows\\Fonts\\segoeui.ttf", 18.0f);
		io.FontDefault = io.Fonts->Fonts.back();*/

		// Setup Dear ImGui style
		ImGui::StyleColorsDark();
		SetDarkThemeColors();
		//ImGui::StyleColorsClassic();

		// This is to make sure that styles are consistent 
		ImGuiStyle& style = ImGui::GetStyle();
		if (io.ConfigFlags & ImGuiConfigFlags_ViewportsEnable)
		{
			style.WindowRounding = 0.0f;
			style.Colors[ImGuiCol_WindowBg].w = 1.0f;
		}
		style.Colors[ImGuiCol_WindowBg] = ImVec4(0.15f, 0.15f, 0.15f, style.Colors[ImGuiCol_WindowBg].w);


		//TODO: Investigate what this is?
		//// Setup scaling
		//
		//style.ScaleAllSizes(main_scale);        // Bake a fixed style scale. (until we have a solution for dynamic style scaling, changing this requires resetting Style + calling this again)
		/*style.FontScaleDpi = main_scale;*/

		Application& app = Application::Get();
		

		// Setup Platform/Renderer bindings
		ImGui_ImplSDL3_InitForOpenGL((SDL_Window*)app.GetWindow().GetNativeWindow() , app.GetWindow().GetRenderContext()->GetNativeContext());
		ImGui_ImplOpenGL3_Init("#version 460");

	}
	void ImGuiSystem::Shutdown()
	{
		// Cleanup
		ImGui_ImplOpenGL3_Shutdown();
		ImGui_ImplSDL3_Shutdown();
		ImGui::DestroyContext();

		// Reset the ImGui context to avoid dangling pointers
		ImGui::SetCurrentContext(nullptr);
	}
